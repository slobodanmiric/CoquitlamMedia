                    <h3 style="padding-bottom: 30px;">Those in Need:</h3>
                    <p>We build trust, friendship, volunteering opportunities to even the most marginalized or at risk immigrants by including them in the mission to rescue food that would otherwise go to waste rather than feel like they’re just on the receiving end of a hand-out.  They are educated regarding what the foods are, how to cook them, how to store them and given a chance to experiment with them.</p><br><br>
                    <p>Participating stores can do their part in reducing unnecessary waste by donating these food products to Immigrant Link Centre who will then distribute them to the needy.  Not only does it improve quality of life for immigrants, but there will be less waste taken to the dump at a cost saving to the stores, the environment will be protected from less garbage, and recipients have a connection to these businesses due to their charitable efforts, thereby adding to their customer base. </p>
